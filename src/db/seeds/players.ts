export type RawPlayer = {
  firstName: string;
  lastName: string;
  position: "GK" | "DEF" | "MID" | "FWD";
  nation: string;
  club: string;
  price: number;
};

function p(
  name: string,
  pos: "GK" | "DEF" | "MID" | "FWD",
  nation: string,
  club: string,
  price: number
): RawPlayer {
  const idx = name.indexOf(" ");
  return {
    firstName: idx === -1 ? name : name.slice(0, idx),
    lastName: idx === -1 ? "" : name.slice(idx + 1),
    position: pos,
    nation,
    club,
    price,
  };
}

