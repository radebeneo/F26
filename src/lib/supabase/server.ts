/**
 * src/lib/supabase/server.ts
 *
 * Server-side Supabase client.
 * Use in Server Components, Route Handlers, and Server Actions.
 * Reads/writes cookies via Next.js `next/headers` to maintain session.
 */

import { createServerClient } from "@supabase/ssr";
import { cookies } from "next/headers";

export async function createClient() {
  const cookieStore = await cookies();

  return createServerClient(
    process.env.NEXT_PUBLIC_SUPABASE_URL!,
    process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY!,
    {
      cookies: {
        getAll() {
          return cookieStore.getAll();
        },
        setAll(cookiesToSet) {
          try {
            cookiesToSet.forEach(({ name, value, options }) =>
              cookieStore.set(name, value, options)
            );
          } catch {
            // setAll is called from a Server Component — safe to ignore
            // (middleware handles session refresh)
          }
        },
      },
    }
  );
}

/**
 * Admin client using the service role key.
 * Use ONLY in server-side code that needs to bypass RLS.
 * ⚠️  Never expose SUPABASE_SERVICE_ROLE_KEY to the client.
 */
